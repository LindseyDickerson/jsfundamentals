// Assignment Operators

// Assignment Operator
let x = 5;
// simply assigning a value to a variable name.
// addition assignment
let x = x + 1;
x += 1;

// Subtraction Assignment
x -= 1
x = x - 1;

// Multiplication Assignment
x = x * 1;
x *= 1;

// Division Assignment
x = x / 1;
x /= 1;

//Remainder/Modulus Assignment
x = x % 1;
x %= 1;

// Exponential Assignment
x = x ** 1;
x **= 1; 
//single '*' is multiplication, Double '**' is exponents