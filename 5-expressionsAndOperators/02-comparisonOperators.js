// Comparison Operators

// equal
'3' == 3;
// 3 string does equal 3 number

// strictly equals
3 === 3;
//9 times out of 10 use strictly equals

// Does not equal
'3' != 4;
//! called a bang

//strictly not equal
'3' !== 3;

// greater than;
3 > 2;

//less than;
2 < 3;

// greater than or equal to
3 >= 2;

// less than or equal to
2 <= 3;

// and
2 && 3;

// or
2 || 3;
// pipe symbol, above return key